    {
        $distrito=new Distrito();
        $distrito->Nro_Distrito=$request->input('NroDistrito');
        $distrito->Nombre=$request->input('Nombre');
        $distrito->Id_Ciudad=$request->input('CodCiudad');
        $distrito->save();

        return redirect()->route('createCiudad');
    }

